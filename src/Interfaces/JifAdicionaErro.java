    public JifAdicionaErro() {
        initComponents();
        
        ErrosDAO dao = new ErrosDAO();
        ArrayList resultadoErros = dao.pequisar();

        DefaultTableModel model = (DefaultTableModel) tblErros.getModel();

        JTable tblErros = new JTable(model);

        model.addColumn("Código");
        model.addColumn("Nome");
        model.setNumRows(0);
        
// ---------------- ATUALIZA TABELA COM ERROS ----------------------
        
        Collection<Erros> lista = resultadoErros;
        Iterator<Erros> it = lista.iterator();
        while (it.hasNext()) {
            Erros error = it.next();
            model.addRow(new String[] {error.getCodigo(), error.getNome()});                
        }
        
        tblErros.setVisible(true);
        
        
    }

