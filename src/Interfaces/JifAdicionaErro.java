    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jButton1.getText() == "Novo"){
            txtNome.setEnabled(true);
            txtDesc.setEnabled(true);
            txtCodigo.setEnabled(true);
            cboPrioridade.setEnabled(true);
            jButton1.setText("Salvar");
        }
        else {
            String codigo = txtCodigo.getText();
            String nome = txtNome.getText();
            String desc = txtDesc.getText();
            String prior = (String) cboPrioridade.getSelectedItem();
            int prioridade = 0;
            switch (prior){
                
                case "Urgente": prioridade = 1;
                    break;
                case "Alta": prioridade = 2;
                    break;
                case "Média": prioridade = 3;
                    break;
                case "Baixa": prioridade = 4;
                    break;
            }

            Erros error = new Erros (codigo, nome, desc, prioridade);
            ErrosDAO dao = new ErrosDAO();
            dao.inserir(error);
            dao.finalizar();
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed


